#include <future>
#include <testsuite_hooks.h>

bool odd(unsigned i) { return i%2; }

void test01()
{
  bool test = false;

  std::packaged_task<bool(unsigned)> p1(odd);

  p1(5);

  try
  {
    p1(4);
  }
  catch (const std::future_error& e)
  {
    VERIFY( e.code() == std::future_errc::promise_already_satisfied );
    test = true;
  }

  VERIFY( test );
}

int main()
{
  test01();
  return 0;
}
